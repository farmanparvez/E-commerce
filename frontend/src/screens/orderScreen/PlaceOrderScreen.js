import React, { useEffect } from "react";
import { Link } from "react-router-dom";
import { Button, Row, Col, ListGroup, Image, Card } from "react-bootstrap";
import { useDispatch, useSelector } from "react-redux";
import Message from "../../components/Message";
import CheckoutSteps from "../../components/CheckoutSteps";
import { createOrder } from "../../redux/actions/productAction";
import { openNotification } from "../../utils/notification";
// import { ORDER_CREATE_RESET } from '../constants/orderConstants'
// import { USER_DETAILS_RESET } from '../constants/userConstants'
import Container from "../../components/Container";
import SpinContainer from "../../components/SpinContainer/SpinContainer";
import "./placeOrder.scss"

const PlaceOrderScreen = ({ history }) => {
  const dispatch = useDispatch();

  const cart = useSelector((state) => state.product);
  const { isLoading, isSuccess, isError, isMessage, order } = cart;

  // console.log(cart.cartItem);
  const cartProduct = cart.cartItem?.filter(
    (val) => val.user === JSON.parse(localStorage.getItem("userInfo"))._id
  );
  if (!cart.shippingAddress.address) {
    history.push("/shipping");
  } else if (!cart.paymentMethod) {
    history.push("/payment");
  }
  //   Calculate prices
  const addDecimals = (num) => {
    return (Math.round(num * 100) / 100).toFixed(2);
  };

  const itemsPrice = addDecimals(
    cartProduct.reduce((acc, item) => acc + item.price * item.qty, 0)
  );
  // console.log(itemsPrice)
  const shippingPrice = addDecimals(itemsPrice > 100 ? 0 : 100);
  const taxPrice = addDecimals(Number((0.15 * itemsPrice).toFixed(2)));
  const totalPrice = (
    Number(itemsPrice) +
    Number(shippingPrice) +
    Number(taxPrice)
  ).toFixed(2);

  // const orderCreate = useSelector((state) => state.orderCreate)
  // const { order, success, error } = orderCreate

  useEffect(() => {
    if (isSuccess) {
      openNotification("success", "Success", isMessage);
      history.push(`/order/${order._id}`);
      // dispatch({ type: USER_DETAILS_RESET })
      // dispatch({ type: ORDER_CREATE_RESET })
    }
    if (isError) openNotification("error", "Failed", isMessage);
    // eslint-disable-next-line
  }, [history, isSuccess, isError, isMessage]);

  // const data = cart.cartItem?.filter(
  //   (val) => val.user === JSON.parse(localStorage.getItem("userInfo"))._id
  // ).map(el => {
  //   const data = {
  //     name: el.name,
  //     qty: el.qty,
  //     image: el.image,
  //     price: el.price,
  //     product: el._id
  //   }
  //   return data
  // })

  // console.log(data)

  const placeOrderHandler = () => {
    dispatch(
      createOrder({
        orderItems: cart.cartItem
          ?.filter(
            (val) =>
              val.user === JSON.parse(localStorage.getItem("userInfo"))._id
          )
          .map((el) => {
            const data = {
              name: el.name,
              qty: el.qty,
              image: el.image,
              price: el.price,
              product: el._id,
            };
            return data;
          }),
        shippingAddress: cart.shippingAddress,
        paymentMethod: cart.paymentMethod,
        itemsPrice,
        shippingPrice,
        taxPrice,
        totalPrice,
      })
    );
  };

  return (
    <Container>
      <div className="place-order-container">
        <SpinContainer spinning={isLoading} value={false}>
          <CheckoutSteps step1 step2 step3 step4 />
          <Row>
            <Col md={8}>
              <ListGroup variant="flush">
                <ListGroup.Item>
                  <h2>Shipping</h2>
                  <p>
                    <strong>Address:</strong>
                    {cart.shippingAddress.address}, {cart.shippingAddress.city}{" "}
                    {cart.shippingAddress.postalCode},{" "}
                    {cart.shippingAddress.country}
                  </p>
                </ListGroup.Item>

                <ListGroup.Item>
                  <h2>Payment Method</h2>
                  <strong>Method: </strong>
                  {cart.paymentMethod}
                </ListGroup.Item>

                <ListGroup.Item>
                  <h2>Order Items</h2>
                  {cart.cartItem.length === 0 ? (
                    <Message>Your cart is empty</Message>
                  ) : (
                    <ListGroup variant="flush">
                      {cartProduct.map((item, index) => (
                        <ListGroup.Item key={index}>
                          <Row>
                            <Col md={1}>
                              <Image
                                src={item.image}
                                alt={item.name}
                                fluid
                                rounded
                              />
                            </Col>
                            <Col>
                              <Link to={`/product/${item.product}`}>
                                {item.name}
                              </Link>
                            </Col>
                            <Col md={4}>
                              {item.qty} x ${item.price} = $
                              {item.qty * item.price}
                            </Col>
                          </Row>
                        </ListGroup.Item>
                      ))}
                    </ListGroup>
                  )}
                </ListGroup.Item>
              </ListGroup>
            </Col>
            <Col md={4}>
              <Card>
                <ListGroup variant="flush">
                  <ListGroup.Item>
                    <h2>Order Summary</h2>
                  </ListGroup.Item>
                  <ListGroup.Item>
                    <Row>
                      <Col>Items</Col>
                      <Col>${itemsPrice}</Col>
                    </Row>
                  </ListGroup.Item>
                  <ListGroup.Item>
                    <Row>
                      <Col>Shipping</Col>
                      <Col>${shippingPrice}</Col>
                    </Row>
                  </ListGroup.Item>
                  <ListGroup.Item>
                    <Row>
                      <Col>Tax</Col>
                      <Col>${taxPrice}</Col>
                    </Row>
                  </ListGroup.Item>
                  <ListGroup.Item>
                    <Row>
                      <Col>Total</Col>
                      <Col>${totalPrice}</Col>
                    </Row>
                  </ListGroup.Item>
                  <ListGroup.Item>
                    {/* {error && <Message variant='danger'>{error}</Message>} */}
                  </ListGroup.Item>
                  <ListGroup.Item>
                    <Button
                      type="button"
                      className="btn-block"
                      disabled={cart.cartItems === 0}
                      onClick={placeOrderHandler}
                    >
                      Place Order
                    </Button>
                  </ListGroup.Item>
                </ListGroup>
              </Card>
            </Col>
          </Row>
        </SpinContainer>
      </div>
    </Container>
  );
};

export default PlaceOrderScreen;
